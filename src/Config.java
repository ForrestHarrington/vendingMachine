
//Class to represent a configuration of the vending machine, holds x and y variables with getters
public class Config {
    private int x;
    private int y;

    Config(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
}
